create table public.ai_speaking_missions (
  id bigint generated by default as identity primary key,
  activity_id bigint not null unique
    references public.lesson_activities (id) on delete cascade,
  config jsonb not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint ai_speaking_missions_config_object
    check (jsonb_typeof(config) = 'object'),
  constraint ai_speaking_missions_config_size
    check (octet_length(config::text) <= 50000),
  constraint ai_speaking_missions_required_shape
    check (
      config ?& array[
        'missionTitle', 'cefrLevel', 'goal',
        'primarySoundLabel', 'primarySoundIpa',
        'primaryWords', 'sentences', 'readingText',
        'supportedTools', 'resultFormatVersion'
      ]
      and jsonb_typeof(config -> 'primaryWords') = 'array'
      and jsonb_typeof(config -> 'secondaryWords') = 'array'
      and jsonb_typeof(config -> 'sentences') = 'array'
      and jsonb_typeof(config -> 'supportedTools') = 'array'
    ),
  constraint ai_speaking_missions_format_version
    check (
      jsonb_typeof(config -> 'resultFormatVersion') = 'number'
      and (config ->> 'resultFormatVersion')::integer = 1
    )
);

alter table public.ai_speaking_missions enable row level security;

grant select on public.ai_speaking_missions to anon, authenticated;
grant insert, update, delete on public.ai_speaking_missions to authenticated;
grant usage, select on sequence public.ai_speaking_missions_id_seq to authenticated;

create policy "ai_speaking_missions_select_published_or_manager"
on public.ai_speaking_missions for select
to anon, authenticated
