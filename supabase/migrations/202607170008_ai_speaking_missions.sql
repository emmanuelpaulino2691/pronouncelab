create policy "ai_speaking_missions_insert_draft"
on public.ai_speaking_missions for insert
to authenticated
with check (
  public.can_edit_drafts()
  and public.is_draft_activity(activity_id)
);

create policy "ai_speaking_missions_update_draft"
on public.ai_speaking_missions for update
to authenticated
using (
  public.can_edit_drafts()
  and public.is_draft_activity(activity_id)
)
with check (
  public.can_edit_drafts()
  and public.is_draft_activity(activity_id)
);

create policy "ai_speaking_missions_delete_draft"
on public.ai_speaking_missions for delete
to authenticated
using (
  public.can_edit_drafts()
  and public.is_draft_activity(activity_id)
);

create trigger ai_speaking_missions_set_updated_at
before update on public.ai_speaking_missions
